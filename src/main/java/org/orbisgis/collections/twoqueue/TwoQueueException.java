package org.orbisgis.collections.twoqueue;

/**
 *
 * @author Antoine Gourlay
 */
public class TwoQueueException extends Exception {

        /**
         * Creates a new instance of <code>TwoQueueException</code> without detail message.
         */
        public TwoQueueException() {
        }

        /**
         * Constructs an instance of <code>TwoQueueException</code> with the specified detail message.
         * @param msg the detail message.
         */
        public TwoQueueException(String msg) {
                super(msg);
        }

        public TwoQueueException(Throwable cause) {
                super(cause);
        }

        public TwoQueueException(String message, Throwable cause) {
                super(message, cause);
        }
}
